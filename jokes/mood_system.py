def get_mood_based_on_time():
    """Определяет настроение Бендера в зависимости от времени суток и дня недели"""
    now = datetime.now()
    hour = now.hour
    weekday = now.weekday()  # 0=понедельник, 6=воскресенье
    
    # Понедельник — особенно агрессивный
    if weekday == 0:
        return Mood.AGGRESSIVE
    
    # Пятница и суббота — пьяный
    if weekday in [4, 5]:
        return Mood.DRUNK
    
    # Раннее утро (6-10) — ленивый
    if 6 <= hour < 10:
        return Mood.LAZY
    
    # День (12-17) — философский
    if 12 <= hour < 17:
        return Mood.PHILOSOPHICAL
    
    # Вечер (20-23) — весёлый
    if 20 <= hour < 23:
        return Mood.HAPPY
    
    # Ночь (23-5) — агрессивный
    if 23 <= hour or hour < 5:
        return Mood.AGGRESSIVE
    
    # По умолчанию — саркастичный
    return Mood.SARCASTIC

def get_joke_by_mood(mood=None):
    """Возвращает шутку в зависимости от настроения"""
    if mood is None:
        mood = get_mood_based_on_time()
    
    jokes = JOKES_BY_MOOD.get(mood, JOKES_BY_MOOD[Mood.SARCASTIC])
    return random.choice(jokes), mood

